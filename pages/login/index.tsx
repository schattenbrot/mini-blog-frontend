import type { GetStaticProps, NextPage } from "next";
import axios, { AxiosResponse } from "axios";
import Head from "next/head";
import { useRouter } from "next/router";
import { FormEventHandler, MouseEventHandler, useState } from "react";
import Button from "../../components/base/Button";
import styles from "../../styles/pages/Login.module.scss";
import useInput from "../../hooks/useInput";
import { useDispatch } from "react-redux";
import { actionCreators } from "../../store";
import { bindActionCreators } from "redux";
import { useTranslation } from "next-i18next";
import { serverSideTranslations } from "next-i18next/serverSideTranslations";

const Login: NextPage = () => {
  const router = useRouter();
  const { t } = useTranslation();
  const dispatch = useDispatch();

  const { loginUser } = bindActionCreators(actionCreators, dispatch);

  const { value: email, bind: bindEmail, reset: resetEmail } = useInput("");
  const {
    value: password,
    bind: bindPassword,
    reset: resetPassword,
  } = useInput("");

  const toRegisterHandler: MouseEventHandler = (event) => {
    event.preventDefault();

    router.push("/register");
  };

  const submitHandler: FormEventHandler = (event) => {
    event.preventDefault();
    (async () => {
      const payload = {
        email,
        password,
      };

      try {
        const response: AxiosResponse<any, any> = await axios.post(
          `/users/login`,
          payload,
          {
            headers: {
              "Content-Type": "application/json",
            },
            withCredentials: true,
          }
        );

        loginUser(response.data.id);
        router.push("/");
      } catch (error) {
        console.error("An error occurred: " + error);
      }
    })();
  };

  const resetHandler: FormEventHandler = (event) => {
    event.preventDefault();

    resetEmail();
    resetPassword();
  };

  return (
    <>
      <Head>
        <title>Login</title>
        <meta name='description' content='Generated by create next app' />
        <link rel='icon' href='/favicon.ico' />
      </Head>

      <div className={styles.container}>
        <h1>{t("login:pageTitle")}</h1>
        <form onSubmit={submitHandler} onReset={resetHandler}>
          <div className={styles["input-element"]}>
            <label htmlFor='email'>{t("login:emailLabel")}</label>
            <input type='email' name='email' id='email' {...bindEmail} />
          </div>
          <div className={styles["input-element"]}>
            <label htmlFor='password'>{t("login:passwordLabel")}</label>
            <input
              type='password'
              name='password'
              id='password'
              {...bindPassword}
            />
          </div>
          <div className={styles.control}>
            <Button type='reset' className='danger'>
              {t("login:resetButton")}
            </Button>
            <Button type='button' className='' onClick={toRegisterHandler}>
              {t("login:toRegisterLink")}
            </Button>
            <Button type='submit' className='ok'>
              {t("login:loginButton")}
            </Button>
          </div>
        </form>
      </div>
    </>
  );
};

export const getStaticProps: GetStaticProps = async ({ locale }) => {
  locale ?? "en";
  return {
    props: {
      ...(await serverSideTranslations(locale!, ["login"])),
    },
  };
};

export default Login;
