import type { NextPage } from "next";
import axios, { AxiosResponse } from "axios";
import Head from "next/head";
import { useRouter } from "next/router";
import { FormEventHandler, MouseEventHandler, useState } from "react";
import Button from "../../components/base/Button";
import styles from "../../styles/pages/Login.module.scss";

const Login: NextPage = () => {
  const router = useRouter();
  const [email, setEmail] = useState<string>("");
  const [password, setPassword] = useState<string>("");

  const emailInputHandler: React.FormEventHandler<HTMLInputElement> = (
    event
  ) => {
    setEmail(event.currentTarget.value);
  };

  const passwordInputHandler: React.FormEventHandler<HTMLInputElement> = (
    event
  ) => {
    setPassword(event.currentTarget.value);
  };

  const toRegisterHandler: MouseEventHandler = (event) => {
    event.preventDefault();

    router.push("/register");
  };

  const submitHandler: FormEventHandler = (event) => {
    event.preventDefault();
    (async () => {
      const payload = {
        email,
        password,
      };

      const response: AxiosResponse<any, any> = await axios.post(
        `/v1/users/login`,
        payload,
        {
          headers: {
            "Content-Type": "application/json",
          },
          withCredentials: true,
        }
      );

      if (!response) {
        console.log("Something horrible happened");
      }

      if (response.status === 200) {
        router.push("/");
        return;
      }
    })();
  };

  const resetHandler: FormEventHandler = (event) => {
    event.preventDefault();

    setEmail("");
    setPassword("");
  };

  return (
    <>
      <Head>
        <title>Login</title>
        <meta name='description' content='Generated by create next app' />
        <link rel='icon' href='/favicon.ico' />
      </Head>

      <div className={styles.container}>
        <h1>Login</h1>
        <form onSubmit={submitHandler} onReset={resetHandler}>
          <div className={styles["input-element"]}>
            <label htmlFor='email'>Email</label>
            <input
              type='email'
              name='email'
              id='email'
              value={email}
              onInput={emailInputHandler}
            />
          </div>
          <div className={styles["input-element"]}>
            <label htmlFor='password'>Password</label>
            <input
              type='password'
              name='password'
              id='password'
              value={password}
              onInput={passwordInputHandler}
            />
          </div>
          <div className={styles.control}>
            <Button type='reset' className='danger'>
              Reset
            </Button>
            <Button type='button' className='' onClick={toRegisterHandler}>
              To Register
            </Button>
            <Button type='submit' className='ok'>
              Login
            </Button>
          </div>
        </form>
      </div>
    </>
  );
};

export default Login;
